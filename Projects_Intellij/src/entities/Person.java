package entities;

public class Person {
    private int s_id;
    private String s_name;
    private String s_FatherLN;
    private String s_MotherLN;
    private String s_sex;
    private String s_mail;
    private String s_direction;
    private int s_dni;

    public int getS_id() {
        return s_id;
    }

    public void setS_id(int s_id) {
        this.s_id = s_id;
    }

    public String getS_name() {
        return s_name;
    }

    public void setS_name(String s_name) {
        this.s_name = s_name;
    }

    public String getS_FatherLN() {
        return s_FatherLN;
    }

    public void setS_FatherLN(String s_FatherLN) {
        this.s_FatherLN = s_FatherLN;
    }

    public String getS_MotherLN() {
        return s_MotherLN;
    }

    public void setS_MotherLN(String s_MotherLN) {
        this.s_MotherLN = s_MotherLN;
    }

    public String getS_sex() {
        return s_sex;
    }

    public void setS_sex(String s_sex) {
        this.s_sex = s_sex;
    }

    public String getS_mail() {
        return s_mail;
    }

    public void setS_mail(String s_mail) {
        this.s_mail = s_mail;
    }

    public String getS_direction() {
        return s_direction;
    }

    public void setS_direction(String s_direction) {
        this.s_direction = s_direction;
    }

    public int getS_dni() {
        return s_dni;
    }

    public void setS_dni(int s_dni) {
        this.s_dni = s_dni;
    }
}
